        self.cnn.eval()
        self.cnn_linear = nn.Linear(2048, config.n_embd)
        self.cnn_ln = nn.LayerNorm(config.n_embd, eps=config.layer_norm_epsilon)
        self.cnn_linear_dropout = nn.Dropout(config.resid_pdrop)
        
        self.category_ctx_drop = nn.Dropout(config.embd_pdrop)
        self.category_ctx_embedding = nn.Embedding(5, config.n_embd)
        #########

